package model;

public class Fatorial_Model {
	
	private int numero;
	private int fat;
	
	public Fatorial_Model(int numero,int fat) {
		
		this.numero=numero;
		this.fat=fat;
		
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public int getFat() {
		return fat;
	}

	public void setFat(int fat) {
		this.fat = fat;
	}
	
	
	
	

}
